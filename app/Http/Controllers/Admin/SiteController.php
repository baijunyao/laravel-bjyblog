     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, Site $siteModel)
    {
        $data = $request->except('_token');
        $result = $siteModel->storeData($data);
        if ($result) {
            // 更新缓存
            Cache::forget('common:site');
        }
        return redirect('admin/site/index');
    }

